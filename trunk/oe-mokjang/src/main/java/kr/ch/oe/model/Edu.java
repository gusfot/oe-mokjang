package kr.ch.oe.model;

public class Edu {

	private Long eduSeq;

	private String eduName;

	public Long getEduSeq() {
		return eduSeq;
	}

	public void setEduSeq(Long eduSeq) {
		this.eduSeq = eduSeq;
	}

	public String getEduName() {
		return eduName;
	}

	public void setEduName(String eduName) {
		this.eduName = eduName;
	}
}